package com.algorithmsoptimization.FibonacciComputation;

public class FibonacciIterative {
    public int calculate(int n) {
        if (n <= 1)
            return n;

        int a = 0, b = 1, sum = 0;
        for (int i = 2; i <= n; i++) {
            sum = a + b;
            a = b;
            b = sum;
        }
        return b;
    }
}
